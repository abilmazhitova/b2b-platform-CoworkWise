import geopandas as gpd
import pandas as pd
from shapely.geometry import Point
import os

# ======================================================
# 1️⃣ Пути к данным
# ======================================================
path_input = "../data/telecom_features.csv"
path_output = "../outputs/density_map.geojson"

# Убедимся, что папка outputs существует
os.makedirs("../outputs", exist_ok=True)

print("🔹 Загрузка агрегированных данных...")
df = pd.read_csv(path_input)
print(f"✅ Загружено строк: {df.shape[0]}, столбцов: {df.shape[1]}")

# ======================================================
# 2️⃣ Создание GeoDataFrame
# ======================================================
print("\n📍 Преобразование координат в геообъекты...")
gdf = gpd.GeoDataFrame(
    df,
    geometry=[Point(xy) for xy in zip(df["LONG_BOT_LEFT"], df["LAT_BOT_LEFT"])],
    crs="EPSG:4326"  # географическая система координат (широта/долгота)
)
print(f"✅ Создан GeoDataFrame: {gdf.shape}")

# ======================================================
# 3️⃣ Проверка статистики
# ======================================================
print("\n📊 Статистика по плотности:")
print(gdf["avg_density"].describe())

# ======================================================
# 4️⃣ Подготовка данных для экспорта
# ======================================================
# Оставляем только нужные колонки
gdf_export = gdf[["ZID_NUMBER", "geometry", "avg_density", "density_score"]]

# ======================================================
# 5️⃣ Сохранение в GeoJSON
# ======================================================
print("\n💾 Сохранение карты плотности в GeoJSON...")
gdf_export.to_file(path_output, driver="GeoJSON")
print(f"📁 Файл успешно сохранён: {path_output}")

# ======================================================
# 6️⃣ Проверка примера
# ======================================================
print("\n🔍 Пример 3 строк:")
print(gdf_export.head(3))

print("\n✅ Готово! Модель плотности (heatmap) создана. "
      "Файл можно подключать к API: /api/heatmap")
