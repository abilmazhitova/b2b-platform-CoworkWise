import pandas as pd

# ===========================================
# 1️⃣ Пути к файлам
# ===========================================
path_input = "../data/telecom_03_04_2023.csv"
path_output = "../data/telecom_cleaned.csv"

print("🔹 Загрузка данных...")
df = pd.read_csv(path_input)

print(f"✅ Загружено строк: {df.shape[0]}, столбцов: {df.shape[1]}")

# ===========================================
# 2️⃣ Проверка структуры и типов
# ===========================================
print("\n📋 Колонки:")
print(list(df.columns))

print("\n🔎 Типы данных:")
print(df.dtypes)

# ===========================================
# 3️⃣ Удаление дубликатов и проверка пропусков
# ===========================================
before = df.shape[0]
df = df.drop_duplicates()
after = df.shape[0]
print(f"\n🧹 Удалено дубликатов: {before - after}")

print("\n🧩 Пропущенные значения (топ-10):")
print(df.isnull().sum().sort_values(ascending=False).head(10))

# ===========================================
# 4️⃣ Фильтрация координат (оставляем только Алматы)
# Алматы: широта ≈ 43–44°, долгота ≈ 76–78°
# ===========================================
mask_valid = (
    (df["LAT_BOT_LEFT"].between(43.0, 44.5)) &
    (df["LONG_BOT_LEFT"].between(76.0, 78.0))
)
invalid_count = (~mask_valid).sum()
print(f"\n⚠️ Строк за пределами Алматы: {invalid_count}")

df = df[mask_valid].reset_index(drop=True)

# ===========================================
# 5️⃣ Преобразование TIME_HOUR (например, '0:00' → 0)
# ===========================================
df["TIME_HOUR"] = df["TIME_HOUR"].astype(str).str.extract(r"(\d+)").astype(int)

# ===========================================
# 6️⃣ Проверка диапазонов численных полей
# ===========================================
for col in ["NUM_OF_UNIQ_USERS", "NUM_OF_UNIQ_HOME_USERS", "NUM_OF_UNIQ_WORK_USERS"]:
    negatives = (df[col] < 0).sum()
    if negatives > 0:
        print(f"⚠️ Найдены отрицательные значения в {col}: {negatives}")
        df = df[df[col] >= 0]

# ===========================================
# 7️⃣ Удаление NaN
# ===========================================
before = df.shape[0]
df = df.dropna()
after = df.shape[0]
print(f"🧹 Удалено строк с NaN: {before - after}")

# ===========================================
# 8️⃣ Базовая статистика
# ===========================================
print("\n📊 Описательная статистика:")
print(df[["NUM_OF_UNIQ_USERS", "NUM_OF_UNIQ_HOME_USERS", "NUM_OF_UNIQ_WORK_USERS"]].describe())

# ===========================================
# 9️⃣ Сохранение очищенных данных
# ===========================================
df.to_csv(path_output, index=False, encoding="utf-8-sig")
print(f"\n📁 Очищенные данные сохранены как: {path_output}")

# ===========================================
# 🔟 Финальная проверка
# ===========================================
print("\n✅ Первые 5 строк очищенных данных:")
print(df.head())

print(f"\n✅ Финальный размер набора: {df.shape}")
